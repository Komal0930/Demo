package com.demo;

public class AreaofCircle {
	
	public static void main (String args[])
	{
	    int radius=3;
	    
	    float pi;
	    pi = 3.14f;
	    
	    		
		float result;
		result =(float) (pi*(radius*radius));
		
		
		System.out.println( "The Area of circle having radius 3 is:"+result);
		
				
	}	

}
