package com.assignments2loop;

public class EvenNoUsingWhileLoop {

	public static void main(String[] args) {
		int num;
		// initializing num value
		num=121;
		//condition
		while (num<=229 )
		{
			//statement
			if(num%2==0)
			{
				System.out.println(num);
				
			}
			num++;
			
		}

	}

}
